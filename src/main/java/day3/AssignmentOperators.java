package day3;

public class AssignmentOperators {


    public static void main (String [] args) {

        // Assignment       =       +=      -=      *=      /=      %-=

        int a=10;
        a+=5;                                           // a=a+5;
        System.out.println(a);

        System.out.println("----------------");

        int b=50;
        b-=15;                                          // b=b-15
        System.out.println(b);

        System.out.println("----------------");

        int c=150;
        c*=50;                                          // c = c*50;
        System.out.println(c);

        System.out.println("----------------");

        int d=1000;
        d/=20;                                          // d = d/20;
        System.out.println(d);

        System.out.println("----------------");

        int e=2000;
        e%=2;                                           // e=e%2;
        System.out.println(e);



    }
}
